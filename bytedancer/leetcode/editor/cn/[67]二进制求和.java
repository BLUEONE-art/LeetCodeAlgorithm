class Solution {
    public String addBinary(String a, String b) {
        StringBuilder sb = new StringBuilder();
        int aIdx = a.length() - 1;
        int bIdx = b.length() - 1;
        int c = 0;
        while (aIdx >= 0 || bIdx >= 0) {
            int n1 = aIdx >= 0 ? a.charAt(aIdx) - '0' : 0;
            int n2 = bIdx >= 0 ? b.charAt(bIdx) - '0' : 0;
            int sum = n1 + n2 + c;
            sb.append(sum % 2);
            c = sum / 2;
            aIdx--;
            bIdx--;
        }
        if (c != 0) {
            sb.append(c);
        }
        return sb.reverse().toString();
    }
}
