class Solution {
    public boolean repeatedSubstringPattern(String s) {
        String str = s + s;
        return str.substring(1, str.length() - 1).contains(s);
    }

    public boolean repeatedSubstringPattern(String s) {
        int len = s.length();
        if(s == null || len < 1) {
            return false;
        }
        int dum = len;
        String str  = s;
        while(dum > 1){
            str = str.charAt(len - 1) + str.substring(0, len - 1);
            if(str.equals(s)) {
                return true;
            }
            dum--;
        }
        return false;
    }
}
