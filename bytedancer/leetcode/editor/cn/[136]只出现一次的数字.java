class Solution {
    public int singleNumber(int[] nums) {
        int len = nums.length;
        if (len <= 1) {
            return nums[0];
        }
        int res = nums[0];
        for (int i = 1; i < len; i++) {
            res ^= nums[i];
        }
        return res;
    }

    public int singleNumber(int[] nums) {
        int len = nums.length;
        int res = nums[0];
        if (len <= 1) {
            return res;
        }
        HashMap<Integer, Integer> map = new HashMap<>();
        for (int num : nums) {
            map.put(num, map.getOrDefault(num, 0) + 1);
        }
        for (int num : nums) {
            if (map.get(num) == 1) {
                res = num;
                break;
            }
        }
        return res;
    }
}
