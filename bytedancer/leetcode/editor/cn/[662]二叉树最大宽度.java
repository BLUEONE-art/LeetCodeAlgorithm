class Solution {
    public int widthOfBinaryTree(TreeNode root) {
        if (root == null) return 0;
        Deque<TreeNode> q = new LinkedList<>(); // åŒç«¯é˜Ÿåˆ—
        root.val = 0; // ç›®çš„æ˜¯ä¸ºäº†è®¡ç®—æ»¡é¡ºåºäºŒå‰æ ‘ä¸ªèŠ‚ç‚¹çš„å€¼ï¼Œç¬¬ä¸€å±‚root = 0ï¼Œroot.left = 2*root + 1 = 1ï¼Œ root.right = 2*root + 2 = 2ï¼Œä¾æ­¤ç±»æ¨æ¯ä¸€å±‚éƒ½æ˜¯é¡ºåºçš„
        int res = 0;
        q.offer(root);
        while (!q.isEmpty()) {
            int levelSize = q.size();
            res = Math.max(res, q.getLast().val - q.getFirst().val + 1); // é¡ºåºçš„æ…¢äºŒå‰æ ‘æœ€å¤§å®½åº¦å°±æ˜¯æœ€åä¸€å±‚æœ€å·¦ã€å³ä¸¤ä¸ªèŠ‚ç‚¹å€¼ä¹‹å·®+1
            while (levelSize > 0) {
                TreeNode cur = q.poll();
                if (cur.left != null) {
                    cur.left.val = 2 * cur.val + 1;
                    q.offer(cur.left);
                }
                if (cur.right != null) {
                    cur.right.val = 2 * cur.val + 2;
                    q.offer(cur.right);
                }
                levelSize--;
            }
        }
        return res;
    }
}
