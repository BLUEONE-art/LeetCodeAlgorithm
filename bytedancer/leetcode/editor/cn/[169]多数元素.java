class Solution {
    public int majorityElement(int[] nums) {
        int vote = 0, x = 0;
        for (int num : nums) {
            if (vote == 0) {
                x = num;
            }
            vote += (x == num) ? 1 : -1;
        }
        return x;
    }
}
