class Solution {
    public int maxSubArray(int[] nums) {
        int n = nums.length;
        int dp_res = nums[0];
        int sum = 0;
        for (int num : nums) {
            if (sum > 0) {
                sum += num;
            }
            else {
                sum = num;
            }
            dp_res = Math.max(dp_res, sum);
        }
        return dp_res;
    }
}
